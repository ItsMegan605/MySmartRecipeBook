package it.unipi.MySmartRecipeBook.model;

public class Admin {

    String username;
    String name;
    String surname;
    String email;
    String password;

    public Admin () {
    }

    public Admin(String username, String name, String surname, String email, String password) {
        this.username = username;
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    //capire se in questi model ci vanno le JSON properties
    //capire che altro serve per l'admin
    // eventualmente adattarlo a lombok ora l'ho fatto io al volo

}
