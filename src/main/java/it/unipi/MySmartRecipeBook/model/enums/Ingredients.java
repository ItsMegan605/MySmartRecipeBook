        public static boolean isValid(String input) {
            if (input == null) return false;
            for (IngredientName ingredient : IngredientName.values()) {
                // equalsIgnoreCase rende il controllo NON case-sensitive
                if (ingredient.name().equalsIgnoreCase(input.trim())) {
                    return true;
                }
            }
            return false;
        }
